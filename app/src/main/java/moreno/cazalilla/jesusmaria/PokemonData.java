package moreno.cazalilla.jesusmaria;

public class PokemonData {
    String name;
    String id;
    int photo;
    String type;
    int weight;
    int height;

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    public int getPhoto() {
        return photo;
    }

    public String getType() {
        return type;
    }

    public int getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }
}
